package ex1_control_statement;

public class Ex7_switch {

	public static void main(String[] args) {

		String greet = "반갑";

		switch (greet) {
		case "안녕":
			System.out.println("하세요");
			break;
		case "반갑":
			System.out.println("습니다");
			break;
		case "미안":
			System.out.println("합니다.");
			break;
				default:
					System.out.println();
			
			
			
			
			
		}// switch

	}// 비교값

}
