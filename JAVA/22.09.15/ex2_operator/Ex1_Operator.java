package ex2_operator;

public class Ex1_Operator {

	public static void main(String[] args) {
		//산술연산자
		int n1 = 20;
		int n2 = 7;
		int n3 = n1 + n2;
		System.out.println("+연산: "+n3);
		
		n3 = n1 - n2;
		System.out.println("-연산: "+n3);
		
		n3 = n1/ n2;
		System.out.println("/연산: "+n3); //몫이 출력된다.
		
		n3 = n1 % n2;
		System.out.println("%연산: "+n3); //나머지가 출력된다.
	
		
		
		
		
		
		
	}

}
